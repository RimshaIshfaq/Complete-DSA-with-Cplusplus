// SUM OF 2 NUMBERS
// This program takes two numbers as input from the user and outputs their sum.

#include <iostream>
using namespace std;

int main(){

    int num1, num2, sum;
    cout << "Enter first number:" << endl;
    cin >> num1;
    cout << "Enter second number:" << endl;
    cin >> num2;
    sum = num1 + num2;
    cout << "The sum is: " << sum << endl;
    return 0;
    
}